# 1부터 16까지 연속된 정수로 행이 4개인 m1행렬 만들기 
m1 <- matrix(1:16, nrow=4)
dim(m1)
m1
# m1행렬의 행과 열을 바꿈
t(m1)

# 행을 기준으로 값이 차례대로 들어가는 m2행렬 생성
m2 <- matrix(1:16, nrow=4, byrow=T)
m2

m1 + 1
m1 * 2
m1 + m2
# m1과 m2에 같은 위치의 값끼리 단순 곱셈
m1 * m2

# m1과 m2의 행렬 곱
m1 %*% m2

