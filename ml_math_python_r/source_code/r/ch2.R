# 역행렬 계산하기
A = matrix(c(2, -5, 4, 1, -2, 1, 1, -4, 6), byrow=T, nrow=3)
# A의 역행렬
B = solve(A)
print(B)

c <- c(-3, 5, 10)
# Ab = c에서 b는 B(A의 역행렬)와 벡터 b를 곱하여 구함
b = B %*% c
b

# 역행렬이 존재하지 않는 행렬 D를 만들고 행렬식 계산
# 역행렬이 존재하지 않는 행렬
D = matrix(c(2, -5, 4, 1, -2, 1, 1, -4, 5), byrow=T, nrow=3)

# solve로 역행렬을 구하며 역행렬이 존재하지 않는 특이행렬(singular)로 결과 출력
solve(D)

# D의 1행을 2배, 2행을 -3배의 합과 D의 3행을 비교
D[1, ] * 2 + D[2, ] * -3
D[3, ]


# 0이 아니면 행들이 선형적 독립, 비특이(정칙) 행렬
det(A)
det(D)
